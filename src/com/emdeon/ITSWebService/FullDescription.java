package com.emdeon.ITSWebService;

public class FullDescription {
    String eligibilityStatusInfo;
    String coverageCode;
    String networkMode;
    String plan;

    public String getEligibilityStatusInfo() {
        return eligibilityStatusInfo;
    }

    public void setEligibilityStatusInfo(String eligibilityStatusInfo) {
        this.eligibilityStatusInfo = eligibilityStatusInfo;
    }

    public String getCoverageCode() {
        return coverageCode;
    }

    public void setCoverageCode(String coverageCode) {
        this.coverageCode = coverageCode;
    }

    public String getNetworkMode() {
        return networkMode;
    }

    public void setNetworkMode(String networkMode) {
        this.networkMode = networkMode;
    }

    public String getPlan() {
        return plan;
    }

    public void setPlan(String plan) {
        this.plan = plan;
    }

}
